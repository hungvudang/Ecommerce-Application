    // Cập nhật danh mục mới
    private void jLabel10MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel10MouseClicked
        int indexRowSelect = jTable1.getSelectedRow();
        try {
            if (indexRowSelect == -1)
                throw new Exception("Chưa chọn danh mục cần cập nhật !");
            long category_id = Long.parseLong(model.getValueAt(indexRowSelect, 0).toString());
            String category_name = jTextField1.getText();
            
            CategoryCotroller.updateCategoryCtrl(category_id, category_name);
            
            ArrayList<Category> categories = CategoryCotroller.getAllCategory();
            MainFrame.setMenuBar(categories);
            dispose();
            new CategorylManagementForm().setVisible(true);
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Cập nhật danh mục thất bại !", "Cập nhật", 0);
            Logger.getLogger(CategorylManagementForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jLabel10MouseClicked

