    // Thêm danh mục
    private void jLabel12MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel12MouseClicked
        
        String category_name = jTextField1.getText();
        try {
            if (category_name.equals(""))
                throw new Exception("Thêm sản phẩm thất bại. Kiểm tra các trường thông tin !");
            
            CategoryCotroller.insertCategoryCtrl(category_name);
            
            ArrayList<Category> categories = CategoryCotroller.getAllCategory();
            MainFrame.setMenuBar(categories);
            
            dispose();
            new CategorylManagementForm().setVisible(true);
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Thêm danh mục thất bại !", "Thêm", 0);
            Logger.getLogger(CategorylManagementForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jLabel12MouseClicked

